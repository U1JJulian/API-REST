/**
 * Suma dos numeros.
 * @author Jose Julian Gonzalez Medrano 
 * @param {number} a - Primer numero a sumar.
 * @param {number} b - Segundo numero a sumar.
 * @returns {number} El resultado de la suma.
 * @example
 * const resultado = sumar(4, 6);
 * console.log(resultado); // 10
 */

export function sumar(a, b) {
  return a + b;
}

/**
 * Resta dos numeros.
 * @author Jose Julian Gonzalez Medrano 
 * @param {number} a - Minuendo.
 * @param {number} b - Sustraendo.
 * @returns {number} El resultado de la resta.
 */

export function restar(a, b) {
  return a - b;
}
